void	ft_string(char *str, int *len)
{
	size_t	i;

	i = 0;
	if (!str)
	{
		write (1, "(NULL)", 6);
		(*len) = (*len) + 6;
		return ;
	}
	if (str[i] != '\0')
	{
		ft_putchar_len(str[i], len);
		i++;
	}
}
